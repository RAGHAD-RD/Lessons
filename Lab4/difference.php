/*
  الفرق بين PDO و MySQLi:

  1. **PDO (PHP Data Objects)**:
     - PDO هو واجهة موحدة للاتصال بمختلف قواعد البيانات مثل MySQL و PostgreSQL و SQLite و SQL Server وغيرها.
     - يمكن استخدام نفس الكود للاتصال بأي قاعدة بيانات تدعمها PDO.
     - يدعم **Prepared Statements** (الاستعلامات المحضرة) ويعزز الأمان ضد هجمات SQL Injection.
     - يدعم **البرمجة الكائنية (OOP)** فقط.
     - يمكن استخدامه مع معاملات (Transactions) باستخدام الدوال مثل `beginTransaction()`, `commit()`, و `rollBack()`.
     - لا يدعم MySQL بشكل خاص، لكنه يعمل مع أي نوع من قواعد البيانات التي تدعمه.

  2. **MySQLi (MySQL Improved)**:
     - MySQLi مُخصص فقط للعمل مع MySQL.
     - يدعم **Prepared Statements** بشكل مشابه لـ PDO، ويعزز الأمان ضد هجمات SQL Injection.
     - يمكن استخدامه بالبرمجة الكائنية (OOP) أو البرمجة الإجرائية (Procedural).
     - يدعم إجراءات مخزنة (Stored Procedures) بشكل كامل.
     - يمكن التعامل مع المعاملات باستخدام دوال مثل `start_transaction()`, `commit()`, و `rollback()`.
     - قد يكون أسرع قليلاً في التعامل مع MySQL مقارنة بـ PDO لأنه مُخصص لهذا النوع من قواعد البيانات.
     - يتيح لك المرونة في استخدام الأسلوب الذي تفضله (OOP أو Procedural).

  3. **الاختيار بينهما**:
     - إذا كنت تعمل مع MySQL فقط وتحتاج إلى ميزات خاصة بـ MySQL مثل الإجراءات المخزنة، قد يكون MySQLi الخيار الأفضل.
     - إذا كنت تخطط للعمل مع أنواع متعددة من قواعد البيانات، أو ترغب في كتابة كود مرن ومتاح للعديد من بيئات قواعد البيانات، فيمكنك اختيار PDO.
*/

